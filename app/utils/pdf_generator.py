def _add_cover_page(story, styles, travel_data):
    """Add a premium cover page to the PDF."""
    story.append(Spacer(1, 2 * cm))

    story.append(Paragraph("✈  TravelPlannerPro", styles["cover_title"]))
    story.append(Paragraph("AI-Powered Travel Itinerary", styles["cover_subtitle"]))
    story.append(Spacer(1, 0.5 * cm))

    source = travel_data.get("source", "Origin")
    destination = travel_data.get("destination", "Destination")
    story.append(Paragraph(f"{source}  →  {destination}", styles["cover_route"]))
    story.append(Spacer(1, 0.8 * cm))

    # Horizontal rule
    story.append(HRFlowable(width="80%", thickness=2, color=GOLD, hAlign="CENTER"))
    story.append(Spacer(1, 0.8 * cm))

    # Trip metadata
    meta_items = [
        ("📅 Departure", travel_data.get("departure_date", "TBD")),
        ("🏠 Return", travel_data.get("return_date", "TBD")),
        ("👥 Travellers", str(travel_data.get("travelers", 1))),
        ("💰 Budget", str(travel_data.get("budget", "TBD"))),
        ("🏨 Accommodation", travel_data.get("hotel_pref", "TBD")),
        ("🚌 Transport", travel_data.get("transport", "TBD")),
    ]

    for label, value in meta_items:
        story.append(Paragraph(f"<b>{label}:</b>  {value}", styles["cover_meta"]))

    story.append(Spacer(1, 1.5 * cm))
    story.append(HRFlowable(width="60%", thickness=1, color=TEXT_MUTED, hAlign="CENTER"))
    story.append(Spacer(1, 0.5 * cm))

    # IBM Granite badge
    story.append(Paragraph(
        "Powered by <b>IBM Granite</b> | IBM watsonx.ai",
        styles["cover_subtitle"],
    ))
    story.append(Paragraph(
        f"Generated on {datetime.now().strftime('%B %d, %Y at %H:%M')}",
        styles["cover_meta"],
    ))
    story.append(Spacer(1, 0.5 * cm))
    story.append(Paragraph(
        "IBM SkillsBuild Final Project · Edunet Foundation",
        styles["cover_meta"],
    ))


def _add_trip_summary(story, styles, travel_data):
    """Add a trip summary table."""
    story.append(Paragraph("📋 Trip Summary", styles["section_heading"]))

    interests = travel_data.get("interests", "Not specified")
    special = travel_data.get("special_requests", "None")

    table_data = [
        [Paragraph("Field", styles["table_header"]), Paragraph("Details", styles["table_header"])],
        ["🛫 From", travel_data.get("source", "—")],
        ["🛬 To", travel_data.get("destination", "—")],
        ["📅 Departure", travel_data.get("departure_date", "—")],
        ["🏠 Return", travel_data.get("return_date", "—")],
        ["👥 Travellers", str(travel_data.get("travelers", 1))],
        ["💰 Budget", travel_data.get("budget", "—")],
        ["🚌 Transport", travel_data.get("transport", "—")],
        ["🏨 Hotel Pref.", travel_data.get("hotel_pref", "—")],
        ["🎯 Interests", interests[:80] + "..." if len(interests) > 80 else interests],
        ["💬 Special Requests", special[:80] + "..." if len(special) > 80 else special],
    ]

    table = Table(table_data, colWidths=[5 * cm, 12 * cm])
    table.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), ACCENT),
        ("TEXTCOLOR", (0, 0), (-1, 0), WHITE),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE", (0, 0), (-1, 0), 10),
        ("ALIGN", (0, 0), (-1, 0), "CENTER"),
        ("BACKGROUND", (0, 1), (-1, -1), BG_LIGHT),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [WHITE, BG_LIGHT]),
        ("FONTNAME", (0, 1), (0, -1), "Helvetica-Bold"),
        ("FONTSIZE", (0, 1), (-1, -1), 9),
        ("GRID", (0, 0), (-1, -1), 0.5, colors.HexColor("#e5e7eb")),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
        ("TOPPADDING", (0, 0), (-1, -1), 6),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 6),
        ("LEFTPADDING", (0, 0), (-1, -1), 8),
        ("RIGHTPADDING", (0, 0), (-1, -1), 8),
        ("ROWHEIGHT", (0, 0), (-1, -1), 22),
    ]))
    story.append(table)


def _add_itinerary_content(story, styles, itinerary_text: str):
    """Parse and add the itinerary text to the PDF."""
    story.append(Paragraph("🗺️ Your Complete Travel Itinerary", styles["section_heading"]))
    story.append(HRFlowable(width="100%", thickness=1, color=ACCENT))
    story.append(Spacer(1, 0.3 * cm))

    lines = itinerary_text.split("\n")

    for line in lines:
        stripped = line.strip()
        if not stripped:
            story.append(Spacer(1, 0.2 * cm))
            continue

        # Detect markdown headings
        if stripped.startswith("### "):
            text = stripped[4:].replace("**", "").replace("*", "")
            story.append(Paragraph(text, styles["sub_heading"]))

        elif stripped.startswith("## "):
            text = stripped[3:].replace("**", "").replace("*", "")
            story.append(Paragraph(text, styles["section_heading"]))

        elif stripped.startswith("# "):
            text = stripped[2:].replace("**", "").replace("*", "")
            story.append(Paragraph(text, styles["section_heading"]))

        elif stripped.startswith("---"):
            story.append(HRFlowable(width="100%", thickness=0.5, color=colors.HexColor("#e5e7eb")))
            story.append(Spacer(1, 0.2 * cm))

        elif stripped.startswith("- ") or stripped.startswith("• "):
            text = stripped[2:].replace("**", "<b>").replace("*", "")
            story.append(Paragraph(f"• {text}", styles["bullet"]))

        elif stripped.startswith("**") and stripped.endswith("**"):
            text = stripped[2:-2]
            story.append(Paragraph(f"<b>{text}</b>", styles["sub_heading"]))

        else:
            # Regular body text — handle basic markdown
            text = stripped.replace("**", "<b>", 1)
            if "<b>" in text:
                text = text.replace("**", "</b>", 1)
            text = text.replace("*", "")
            try:
                story.append(Paragraph(text, styles["body"]))
            except Exception:
                story.append(Paragraph(stripped, styles["body"]))
