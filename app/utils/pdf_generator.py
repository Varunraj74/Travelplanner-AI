"""
TravelPlannerPro - PDF Generator
Creates professional travel reports using ReportLab.
"""

import io
import os
import logging
from datetime import datetime
from typing import Dict, Any

logger = logging.getLogger(__name__)

try:
    from reportlab.lib import colors
    from reportlab.lib.pagesizes import A4
    from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
    from reportlab.lib.units import cm, mm
    from reportlab.lib.enums import TA_CENTER, TA_LEFT, TA_RIGHT, TA_JUSTIFY
    from reportlab.platypus import (
        SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
        HRFlowable, PageBreak, KeepTogether,
    )
    from reportlab.platypus.flowables import HRFlowable
    REPORTLAB_AVAILABLE = True
except ImportError:
    REPORTLAB_AVAILABLE = False
    logger.warning("ReportLab not installed. PDF export unavailable.")


# ─── Brand Colors (only defined when ReportLab is available) ──────────────────
if REPORTLAB_AVAILABLE:
    PRIMARY    = colors.HexColor("#1a1a2e")
    ACCENT     = colors.HexColor("#0f3460")
    GOLD       = colors.HexColor("#e94560")
    LIGHT_BLUE = colors.HexColor("#16213e")
    TEXT_DARK  = colors.HexColor("#1f2328")
    TEXT_MUTED = colors.HexColor("#57606a")
    BG_LIGHT   = colors.HexColor("#f7f8fa")
    WHITE      = colors.white
    BLACK      = colors.black


def generate_travel_pdf(
    travel_data: Dict[str, Any],
    itinerary_text: str,
    output_path: str,
) -> bool:
    """
    Generate a professional travel report PDF.

    Args:
        travel_data: Dict with trip details (source, destination, dates, etc.)
        itinerary_text: Raw itinerary markdown/text from IBM Granite.
        output_path: Full path where the PDF will be saved.

    Returns:
        True on success, False on failure.
    """
    if not REPORTLAB_AVAILABLE:
        logger.error("ReportLab not available for PDF generation.")
        return False

    try:
        os.makedirs(os.path.dirname(output_path), exist_ok=True)
        doc = SimpleDocTemplate(
            output_path,
            pagesize=A4,
            rightMargin=2 * cm,
            leftMargin=2 * cm,
            topMargin=2 * cm,
            bottomMargin=2 * cm,
            title="Travel Itinerary — TravelPlannerPro",
            author="TravelPlannerPro | IBM Granite",
        )

        styles = _build_styles()
        story = []

        # Cover Page
        _add_cover_page(story, styles, travel_data)
        story.append(PageBreak())

        # Trip Summary Table
        _add_trip_summary(story, styles, travel_data)
        story.append(Spacer(1, 0.5 * cm))

        # Main Itinerary Content
        _add_itinerary_content(story, styles, itinerary_text)

        # Footer Note
        story.append(Spacer(1, 1 * cm))
        story.append(HRFlowable(width="100%", thickness=1, color=ACCENT))
        story.append(Spacer(1, 0.3 * cm))
        story.append(Paragraph(
            "Generated by <b>TravelPlannerPro</b> · Powered by <b>IBM Granite</b> via IBM watsonx.ai<br/>"
            "IBM SkillsBuild Final Project | Edunet Foundation | Problem Statement No. 5",
            styles["footer_note"],
        ))

        doc.build(story)
        logger.info(f"PDF generated: {output_path}")
        return True

    except Exception as e:
        logger.error(f"PDF generation failed: {e}")
        return False


